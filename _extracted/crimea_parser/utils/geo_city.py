def detect_city_by_coords(lat: Optional[float], lon: Optional[float]) -> str:
    if lat is None or lon is None:
        return ""
    for city, lat_min, lat_max, lon_min, lon_max in CITY_BBOXES:
        if lat_min <= lat <= lat_max and lon_min <= lon <= lon_max:
            return city
    # точка в Крыму, но не в одном из bbox — большой район
    if 44.0 <= lat <= 46.5 and 32.0 <= lon <= 37.0:
        return ""
    return ""
