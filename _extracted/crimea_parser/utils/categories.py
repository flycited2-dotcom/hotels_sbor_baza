"""Нормализация category → client_type.

`category` приходит из разных источников в разном написании
(«отель», «Отель», «hotel», «апарт-отель»). Для группировки и раскраски
в Excel приводим к канонической форме.
"""

CANONICAL = [
    "отель",
    "гостиница",
    "апарт-отель",
    "апартаменты",
    "хостел",
    "пансионат",
    "санаторий",
    "база отдыха",
    "дом отдыха",
    "гостевой дом",
    "эллинг",
    "глэмпинг",
    "кемпинг",
    "мотель",
]

DEFAULT = "прочее"

ALIASES = {
    "отель": "отель",
    "hotel": "отель",
    "гостиница": "гостиница",
    "апарт-отель": "апарт-отель",
    "апарт отель": "апарт-отель",
    "aparthotel": "апарт-отель",
    "апартаменты": "апартаменты",
    "apartment": "апартаменты",
    "хостел": "хостел",
    "hostel": "хостел",
    "пансионат": "пансионат",
    "санаторий": "санаторий",
    "база отдыха": "база отдыха",
    "дом отдыха": "дом отдыха",
    "гостевой дом": "гостевой дом",
    "guest house": "гостевой дом",
    "эллинг": "эллинг",
    "глэмпинг": "глэмпинг",
    "кемпинг": "кемпинг",
    "camping": "кемпинг",
    "мотель": "мотель",
    "motel": "мотель",
    "кров и завтрак": "гостевой дом",
    "bed and breakfast": "гостевой дом",
}


def normalize(category: str) -> str:
    """Привести category к канонической client_type. Пустое → 'прочее'."""
    if not category:
        return DEFAULT
    cat = category.lower().strip()
    if cat in ALIASES:
        return ALIASES[cat]
    for key, val in ALIASES.items():
        if key in cat:
            return val
    return DEFAULT
