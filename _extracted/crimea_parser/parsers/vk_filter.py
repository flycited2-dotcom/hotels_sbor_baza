"""Классификатор VK-групп: hotel / noise / ambiguous.

Решение по двум полям:
  - name (название группы)
  - activity (категория VK: «Гостиница», «Магазин», «Услуги» и т.п.)

Логика:
  has_hotel = есть отельное ключевое слово → "hotel"
  иначе has_noise = есть шумовое слово → "noise"
  иначе → "ambiguous" (сохраняем с пометкой)

Это консервативная классификация: реальный отель попадёт в "noise" только
если у него ОДНОВРЕМЕННО в названии/activity нет ни одного отельного слова
И есть шумовое — практически невозможно.
"""

HOTEL_KEYWORDS = (
    # стемы (без последней буквы) — ловят и единственное, и множественное число
    "отел",        # отель, отели, отелями, отельный
    "гостиниц",    # гостиница, гостиницы
    "хостел",      # хостел, хостелы
    "санатор",     # санаторий, санатории
    "пансионат",   # пансионат, пансионата
    "размещен",    # размещение, размещения
    "апарт",       # апартаменты, апарт-отель, апартотель
    "мотел",       # мотель, мотели
    "усадьб",      # усадьба, усадьбы
    "коттедж",
    "глэмпинг", "кемпинг", "эллинг",
    "турбаз", "туркомплекс", "здравниц",
    # фразы — целиком
    "дом отдыха", "база отдыха", "гостевой дом",
    # точечные формы
    "вилла", "виллы",
)

NOISE_KEYWORDS = (
    # коммерция / услуги / товары
    "магазин", "торговля", "товаров", "услуги для",
    "ремонт", "строительство", "юрист", "адвокат",
    "недвижимости", "риелтор", "застройщик",
    "продажа", "интернет-магазин", "оптом",
    # транспорт / прокат
    "аренда авто", "прокат авто", "такси", "трансфер", "доставка",
    # event-сегмент
    "свадьб", "банкет", "ресторан", "кафе ", "кофейн", "бар ",
    # фитнес/здоровье/красота
    "фитнес", "стоматолог", "клиника", "красот", "парикмахер", "косметолог",
    # образование/детское
    "школа", "детский сад", "развивающ",
    # туризм-агентства (не отели сами по себе)
    "туристическое агентство", "туроператор", "турагент", "экскурс",
    # бытовка
    "мебель", "одежда", "цветы", "ателье", "фотограф", "видеограф",
    "рукоделие",
    # коммьюнити / контент
    "знакомств", "чат", "юмор",
    "новости", "афиша", "объявлен", "консалтинг", "тренинг",
    # частые noise-марки в названиях
    "тент-мастер", "клининг", "уборка", "химчист",
)


def classify(name: str, activity: str) -> str:
    """'hotel' | 'noise' | 'ambiguous'."""
    t = f"{(name or '').lower()} | {(activity or '').lower()}"
    if any(kw in t for kw in HOTEL_KEYWORDS):
        return "hotel"
    if any(kw in t for kw in NOISE_KEYWORDS):
        return "noise"
    return "ambiguous"
